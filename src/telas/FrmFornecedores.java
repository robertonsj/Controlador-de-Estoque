    public void listar() {

        /*Criação de um objeto do tipo 'ClienteDadosTratados' para o método listarClientes()
        possa ser executado e o resultado armazenado em uma lista.
        Para posteriormente, utilizando-se de um objeto do tipo 'DefaultTableModel', eles possam
        ser exibidos no formulario 'FrmClientes'.*/
        FornecedorDadosTratados fdt = new FornecedorDadosTratados();
        List<Fornecedor> lista = fdt.listarTodos();
        DefaultTableModel dados = (DefaultTableModel) tblFornecedores.getModel();
        dados.setNumRows(0);

        for (Fornecedor c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCnpj(),
                c.getEmail(),
                c.getContato(),
                c.getEndereco(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf()

            });
        }

    }
    
