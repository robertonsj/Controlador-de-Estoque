    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // botão editar

        Cliente obj = new Cliente();

        obj.setCpf(txtcpf.getText());
        obj.setNome(txtnome.getText());
        obj.setEmail(txtemail.getText());
        obj.setContato(txtcontato.getText());
        obj.setEndereco(txtendereco.getText());
        obj.setComplemento(txtcomplemento.getText());
        obj.setBairro(txtbairro.getText());
        obj.setCidade(txtcidade.getText());
        obj.setUf(cbuf.getSelectedItem().toString());

        obj.setId(Integer.parseInt(txtcodigo.getText()));

        //Objeto criado para a atualização do registro ser feita.
        ClienteDadosTratados data = new ClienteDadosTratados();
        data.atualizarCliente(obj);

        new Utilitarios().LimpaTela(painelclientedados);

    }//GEN-LAST:event_btneditarActionPerformed

