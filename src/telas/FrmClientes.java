    private void btnListarPorNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarPorNomeActionPerformed
        // TODO add your handling code here:
        
        /*Criação de um objeto do tipo 'ClienteDadosTratados' para o método listarClientes()
        possa ser executado e o resultado armazenado em uma lista.
        Para posteriormente, utilizando-se de um objeto do tipo 'DefaultTableModel', eles possam
        ser exibidos no formulario 'FrmClientes'.*/
        
        List<Cliente> lista = new ArrayList<>();
        ClienteDadosTratados dadostd = new ClienteDadosTratados();
        
        String nome = "%"+txtNome.getText()+"%";
        
        
        lista = dadostd.listarPorNome(nome);
        
        DefaultTableModel dados = (DefaultTableModel) tblClientes.getModel();
        dados.setNumRows(0);

        for (Cliente c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCpf(),
                c.getEmail(),
                c.getContato(),
                c.getEndereco(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf()

            });
        }
    }//GEN-LAST:event_btnListarPorNomeActionPerformed

