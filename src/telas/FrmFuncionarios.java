    private void btn_pesquisarcpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarcpfActionPerformed
        // TODO add your handling code here:

        String cpf = txtcpf.getText();
        Funcionario obj = new Funcionario();
        FuncionarioDadosTratados dados = new FuncionarioDadosTratados();

        obj = dados.consultarPorCpf(cpf);
        
        if(obj.getCpf() != null){
        //exibindo os dados do objeto nos campos de texto
        txtcodigo.setText(String.valueOf(obj.getId()));
        txtnome.setText(obj.getNome());
        txtcpf.setText(obj.getCpf());
        txtemail.setText(obj.getEmail());
        txtsenha.setText(obj.getSenha());
        txtcargo.setText(obj.getCargo());
        cbnivelacesso.setSelectedItem(obj.getNivel_acesso());
        txtcontato.setText(obj.getContato());
        txtendereco.setText(obj.getEndereco());
        txtcomplemento.setText(obj.getComplemento());
        txtbairro.setText(obj.getBairro());
        txtcidade.setText(obj.getCidade());
        cbuf.setSelectedItem(obj.getUf());
        
        }else{
            JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
        }

    }//GEN-LAST:event_btn_pesquisarcpfActionPerformed

