    private void btnPesquisarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarNomeActionPerformed
        // TODO add your handling code here:
        String nome = "%"+txtNome.getText()+"%";
        FuncionarioDadosTratados funcDt = new FuncionarioDadosTratados();
        List<Funcionario> lista = funcDt.listarPorNome(nome);
        
        DefaultTableModel dados = (DefaultTableModel) tblFunc.getModel();
        dados.setNumRows(0);
        
        for(Funcionario f : lista){
            
            dados.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCpf(),
                f.getEmail(),
                f.getSenha(),
                f.getCargo(),
                f.getNivel_acesso(),
                f.getContato(),
                f.getEndereco(),
                f.getComplemento(),
                f.getBairro(),
                f.getCidade(),
                f.getUf()
                
            });
            
        }
        
    }//GEN-LAST:event_btnPesquisarNomeActionPerformed

