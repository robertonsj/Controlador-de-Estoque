    public void listarNaJTable() {

        /*Foi criado o obj 'ClienteDadosTratados' para ser usado o método listarClientes().
        Em uma lista de clintes é atruída todos os registros de clientes da tabela 'tb_clientes'.
        Um obj do tipo 'DefaultTableModel' é criado para receber todas ocorrências da tabela 'tb_funcionarios'.*/
        FuncionarioDadosTratados dadosdt = new FuncionarioDadosTratados();
        List<Funcionario> lista = dadosdt.listarFuncionarios();
        DefaultTableModel dados = (DefaultTableModel) tblFunc.getModel();
        dados.setNumRows(0);//setado o valor zero ao número de linhas.

        //Utilização do for-each para add as ocorrências da fonte de dados no JTable.
        for (Funcionario c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCpf(),
                c.getEmail(),
                c.getSenha(),
                c.getCargo(),
                c.getNivel_acesso(),
                c.getContato(),
                c.getEndereco(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf()

            });
        }

    }

