    //Listando todos os produtos na JTable do Formulário de Produtos 'FrmProdutos'.
    public void listarTodosNaJTable(){
        List<Produto> lista = new ArrayList<>();
        ProdutoDadosTratados pdt = new ProdutoDadosTratados();
        lista = pdt.listarTodos();
        
        DefaultTableModel dados = (DefaultTableModel) tblProdutos.getModel();
        
        //limpa o tipo 'DefaultTableModel'.
        dados.setNumRows(0);         
        
        for(Produto p : lista){
            dados.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtdEstoque(),
                p.getFornecedor().getNome()
 
            });
        }
        
    }
    

