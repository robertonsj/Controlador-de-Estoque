    private void btnPesquisarDescricaoGuia1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarDescricaoGuia1ActionPerformed
        // TODO add your handling code here:
        String descricao = txtdescricao.getText();
               
        ProdutoDadosTratados dadosP = new ProdutoDadosTratados();
        Produto objP = new Produto();
        
        objP = dadosP.pesquisarPorDescricao(descricao);
        
        cbFornecedores.removeAllItems();
        
        if(objP.getDescricao() != null){
            
            //Os dados do Produto buscado serão setados nos campos do formulário.
            txtcodigo.setText(String.valueOf(objP.getId()));
            txtdescricao.setText(objP.getDescricao());
            txtpreco.setText(String.valueOf(objP.getPreco()));
            txtestoque.setText(String.valueOf(objP.getQtdEstoque()));
            
            //cbFornecedores.setSelectedItem(obj.getFornecedor());
            Fornecedor objF = new Fornecedor();
            FornecedorDadosTratados dadosF = new FornecedorDadosTratados();
            
            objF = dadosF.pesquisarPorNome(objP.getFornecedor().getNome());
            
            cbFornecedores.getModel().setSelectedItem(objF);
            
            
        }else{
            JOptionPane.showMessageDialog(null,"Produto não encontrado!");
        }
        
        
    }//GEN-LAST:event_btnPesquisarDescricaoGuia1ActionPerformed

