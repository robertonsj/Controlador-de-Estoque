    private void tblProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutosMouseClicked
        //pega os dados
        painelComGuias.setSelectedIndex(0);
        
        txtcodigo.setText(tblProdutos.getValueAt(tblProdutos.getSelectedRow(),0).toString());
        txtdescricao.setText(tblProdutos.getValueAt(tblProdutos.getSelectedRow(),1).toString());
        txtpreco.setText(tblProdutos.getValueAt(tblProdutos.getSelectedRow(),2).toString());
        txtestoque.setText(tblProdutos.getValueAt(tblProdutos.getSelectedRow(),3).toString());
        
        /*Dois objetos foram criados: um objeto 'Fornecedor' e um obj 'FornecedorDadosTratados'.
        obj Fornecedor: irá armazenar o resultado da busca pelo nome do fornecedor.
        obj FornecedorDadosTratados: criado para que método responsável pela busca seja implementado.*/
        Fornecedor objF = new Fornecedor();
        FornecedorDadosTratados dados = new FornecedorDadosTratados();
        String nome = tblProdutos.getValueAt(tblProdutos.getSelectedRow(),4).toString();
        objF = dados.pesquisarPorNome(nome);                
        
        cbFornecedores.removeAllItems();
        cbFornecedores.getModel().setSelectedItem(objF);
        
        
    }//GEN-LAST:event_tblProdutosMouseClicked

